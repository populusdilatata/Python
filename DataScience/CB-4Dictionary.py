# Alter a Dictionary

STDMarks={"Alice":50, "Ctirad":80, "Drahoslav":90}
print (STDMarks)
# {'Alice': 50, 'Ctirad': 80, 'Drahoslav': 90}
del STDMarks['Drahoslav'] # remove entry with key 'Abdullah Mohamed'
print (STDMarks)
# {'Alice': 50, 'Ctirad': 80}
STDMarks.clear() # remove all entries in STDMarks dictionary
print (STDMarks)
# {}
del STDMarks # delete entire dictionary
