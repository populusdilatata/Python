# Slicing Tuple Values
MarksCIS = (88, 65, 70,90,85,45,78,95,55)
print ("\nForward slicing")
print (MarksCIS[1:4])
# (65, 70, 90)
print (MarksCIS[:3])
# (88, 65, 70) 
print (MarksCIS[6:])
# (78, 95, 55)
print (MarksCIS[4:6])
# (85, 45)
print ("\nBackward slicing")
print (MarksCIS[-4:-2])
# (45, 78)
print (MarksCIS[-3])
# 78
print (MarksCIS[-3:])
# (78, 95, 55)
print (MarksCIS[ :-3])
# (88, 65, 70, 90, 85, 45)